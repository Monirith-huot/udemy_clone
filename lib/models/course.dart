class Course {
  final String title;
  final String instructorName;
  final double price;
  final double rating;
  final String image;
  final int students;
  final String type;

  const Course({
    required this.title,
    required this.instructorName,
    required this.price,
    required this.rating,
    required this.image,
    required this.students,
    required this.type,
  });
}
