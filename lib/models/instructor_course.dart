class InstructorCourse {
  final String title;
  final String instructorName;
  final double price;
  final String image;
  final double rating;
  final String type;
  final int students;

  const InstructorCourse({
    required this.title,
    required this.instructorName,
    required this.price,
    required this.image,
    required this.rating,
    required this.type,
    required this.students,
  });
}
